<template>
  <div class="videoWrapper">
      <iframe
      width="560"
      height="315"
      :src="videoURL"
      title="YouTube video player"
      frameborder="0"
      allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
      allowfullscreen
    ></iframe>
  </div>
</template>

<script>
export default {
  props: ["vid"],
  computed: {
    videoURL() {
      return `https://www.youtube.com/embed/${this.vid}`
    }
  }
}
</script>

